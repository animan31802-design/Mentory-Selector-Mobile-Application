package com.example.admin;

public class ThreeStrings {
    private final String s1;
    private final String s2;
    private final String s3;

    public ThreeStrings(String s1, String s2, String s3) {
        this.s1 = s1;
        this.s2 = s2;
        this.s3 = s3;
    }

    public String getS1() {
        return s1;
    }

    public String getS2() {
        return s2;
    }

    public String getS3() {
        return s3;
    }
}